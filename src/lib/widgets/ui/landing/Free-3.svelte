                      <h3>Lower the environmental impact of turfgrass</h3>
                      <p>Our Hydroponic system is efficient and enables a lower environmental impact of turfgrass, according to an independent Life Cycle Analysis. Here are a few of the environmental issues the Capillary Hydroponics address successfully.</p>
                    </div>
