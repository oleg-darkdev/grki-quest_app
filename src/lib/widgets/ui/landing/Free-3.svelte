                      <div class="col_wrap">
                        <div class="tag_green">Water management</div>
                        <h3>Automated collection and distribution of water</h3>
                        <p class="subhead">Naturally collect rainwater and decrease risk of flooding or overload on sewage and day water systems in cities</p>
                        <p class="paragraph">The Hydroponic Basins act as a natural aid to heavy precipitation events and help unload the burden of other water systems. Collecting and reusing the water helps decrease the water consumption.</p>
