                        <div class="tag_green">Life Cycle Analysis - LCA</div>
                        <h3>Hydroponic turfs for a lower carbon footprint</h3>
                        <p class="subhead">With a lower maintenance and less need for renovation the environmental impact of a hydroponic turf is significantly less</p>
                        <p class="paragraph">According to the LCA Analysis, hydroponic lawns have the lowest annual maintenance and lower need for renovation. They also have lower consumption of water and fertilizers, reducing the stress on the ground, drinking water supply and risk of eutrophication. These features make hydroponic lawns a more sustainable and beneficial alternative to traditional grass lawns.</p>
