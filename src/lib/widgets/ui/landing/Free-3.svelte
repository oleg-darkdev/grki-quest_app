                        <div class="tag_green">Enclosed natural biofilter</div>
                        <h3>Strain, sift and filter water automatically</h3>
                        <p class="subhead">Using an enclosed system with natural sand and underground basins, it filters all water naturally from air and water pollution and then collects it to be reused.</p>
                        <p class="paragraph">By using subsurface irrigation and distribution of fertilizers and pesticides in the water there is no leaching of harmful substances or microplastics into adjacent soil, surrounding water streams or ground water.</p>
