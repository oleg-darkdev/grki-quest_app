                        <div class="tag_green">Research and reference project: Water consumption</div>
                        <h3>Studies indicate 85% less water consumption</h3>
                        <p class="subhead">Several installations over many years prove significant water savings compared to traditional overhead irrigation systems. </p>
                        <p class="paragraph">At MSU and the Hancock Turfgrass Research Center, a multiple year study with 6 identical putting-greens was started in 2022. 3 of the greens are built with the USGA variable root-zone depth method and 3 are built with the Capillary Hydroponics system. Initial results are very promising and indicate similar levels of water savings previously recorded.</p>
